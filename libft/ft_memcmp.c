#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	char	*str1;
	char	*str2;

	i = 0;
	str1 = (char *)s1;
	str2 = (char *)s2;
	if (n == 0)
		return (0);
	while (i + 1 < n && str1[i] == str2[i] && str1[i] != 0 && str2[i] != 0)
		i++;
	return ((unsigned char)str1[i] - (unsigned char)str2[i]);
}

